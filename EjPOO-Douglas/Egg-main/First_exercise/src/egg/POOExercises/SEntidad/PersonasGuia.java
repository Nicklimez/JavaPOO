package egg.POOExercises.SEntidad;

public class PersonasGuia {

    /*
    ?Nombre
    ?Numero ID
    */

    String name;
    int IDNumber;

    public PersonasGuia() {
        this.name = name;
        this.IDNumber = IDNumber;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getIDNumber() {
        return IDNumber;
    }

    public void setIDNumber(int IDNumber) {
        this.IDNumber = IDNumber;
    }
}
